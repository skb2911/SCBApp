package com.example.subhamtandon.scbapp;

public class ListItemChapters {

    private String chapterHead, chapterDesc;

    public ListItemChapters(String chapterHead, String chapterDesc) {
        this.chapterHead = chapterHead;
        this.chapterDesc = chapterDesc;
    }

    public String getChapterHead() {
        return chapterHead;
    }

    public String getChapterDesc() {
        return chapterDesc;
    }
}
